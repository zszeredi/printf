#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	int	i;

	if (s1 && s2)
	{
		i = -1;
		while (s1[++i] != '\0' && s2[i] != '\0')
			if (s1[i] != s2[i])
				return (0);
		if (s1[i] == s2[i])
			return (1);
		return (0);
	}
	return (0);
}
