#include "libft.h"

void	ft_putendl(char const *s)
{
	int	index;

	if (s)
	{
		index = 0;
		while (s[index] != '\0')
		{
			write(1, s, 1);
			s++;
		}
		write(1, "\n", 1);
	}
}
