#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	size_t	a;
	size_t	b;

	a = 0;
	b = 0;
	while (src[a] != '\0')
	{
		dst[b] = src[a];
		a++;
		b++;
	}
	dst[b] = '\0';
	return (dst);
}
