#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t		x;
	char		*start;

	x = 0;
	start = s1;
	while (*s1 != '\0')
	{
		s1++;
	}
	while (s2[x] != '\0' && x < n)
	{
		s1[x] = s2[x];
		x++;
	}
	s1[x] = '\0';
	return (start);
}
