#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	size_t	n;

	n = nb;
	if (nb < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putchar_fd(((n % 10) + 48), fd);
	}
	else
		ft_putchar_fd((n + 48), fd);
}
