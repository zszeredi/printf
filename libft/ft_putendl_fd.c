#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	int	index;

	if (s)
	{
		index = 0;
		while (s[index] != '\0')
		{
			write(fd, s, 1);
			s++;
		}
		write(fd, "\n", 1);
	}
}
